//	    Recap: 
//		Variables in Java: 
//			
//			static	- this variable belongs to the class; for all the objects there is just one copy
//						of this variable
//			local	- a variable used only in a block of code
//			instance - this variable depends on each instance/obj
//
//			for the methods:
//			static - this method belongs to the class
//					- cannot contain instance variable
//			instance - these are different methods for every object
//			
//			
//			
//	Today: Access modifier
//			
//			private --> the most restrictive
//			default --> we don't need to write it*** elements are seen only within the same package
//			protected -- same as default, except inheritance
//			public --> elements are seen from any package in the project
//			
//			
//			Syntax of a variable:
//			OPTIONAL				MUST			MUST
//			access modifier 		datatype		name
//			
//			
//			Syntax of a method
//			OPTIONAL (default)		MUST			MUST		MUST
//			access modifier      return type		name	()			{}
